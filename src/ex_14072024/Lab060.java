package ex_14072024;

public class Lab060 {
    public static void main(String[] args) {

        // Increment (++) , Decrement (--) operators
        // pre , post

        int a =10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);

        int a1=20;
        System.out.println(++a1);

        int a2=30;
        System.out.println(a2++);




    }
}
